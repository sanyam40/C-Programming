/* 21. Write a program in C to print the table of a given number which is multiplied from 1 to
10 using for loop
CODE : */ 

#include<stdio.h>
int main()
{
int i=0,j=0;
for(i=4;i<=4;i++)
{
printf("Table for %d\n",i);
for(j=1;j<=10;j++)
{
printf("%d*%d = %d\n",i,j,(i*j));
}
printf("\n");
}
return 0;
}